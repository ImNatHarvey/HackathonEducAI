import { useEffect, useMemo, useState } from "react";
import type { StudySlide } from "../../../lib/n8n";

type SavedSlidesDeck = {
  title?: string;
  description?: string;
  slides?: StudySlide[];
};

type SavedSlidesResultProps = {
  data?: {
    deck?: SavedSlidesDeck;
    result?: {
      deck?: SavedSlidesDeck;
    };
    provider?: string;
    fallback?: boolean;
    [key: string]: unknown;
  };
};

function getDeckPayload(data?: SavedSlidesResultProps["data"]): SavedSlidesDeck {
  if (!data) return {};

  if (data.deck && typeof data.deck === "object") {
    return data.deck;
  }

  if (
    data.result &&
    typeof data.result === "object" &&
    data.result.deck &&
    typeof data.result.deck === "object"
  ) {
    return data.result.deck;
  }

  return {};
}

function normalizeSlide(slide: StudySlide, index: number): StudySlide {
  return {
    ...slide,
    slideNumber: Number(slide.slideNumber) || index + 1,
    title: slide.title || `Slide ${index + 1}`,
    subtitle: slide.subtitle || "",
    bullets: Array.isArray(slide.bullets) ? slide.bullets.filter(Boolean) : [],
    speakerNotes: slide.speakerNotes || "",
    visualIdea: slide.visualIdea || "",
  };
}

function formatProvider(provider?: unknown): string {
  if (typeof provider !== "string" || !provider.trim()) return "AI";
  return provider.trim().toUpperCase();
}

export default function SavedSlidesResult({ data }: SavedSlidesResultProps) {
  const [activeIndex, setActiveIndex] = useState(0);
  const [showSpeakerNotes, setShowSpeakerNotes] = useState(false);

  const deck = getDeckPayload(data);

  const slides = useMemo(() => {
    return Array.isArray(deck.slides)
      ? deck.slides.map((slide, index) => normalizeSlide(slide, index))
      : [];
  }, [deck.slides]);

  const activeSlide = slides[activeIndex];
  const totalSlides = slides.length;
  const progressPercent =
    totalSlides > 0 ? ((activeIndex + 1) / totalSlides) * 100 : 0;

  const provider = formatProvider(data?.provider);

  const deckTitle =
    typeof deck.title === "string" && deck.title.trim()
      ? deck.title.trim()
      : "Generated Slide Deck";

  const deckDescription =
    typeof deck.description === "string" && deck.description.trim()
      ? deck.description.trim()
      : "A generated slide deck based on your selected study source.";

  const goToPrevious = () => {
    setShowSpeakerNotes(false);
    setActiveIndex((current) => Math.max(current - 1, 0));
  };

  const goToNext = () => {
    setShowSpeakerNotes(false);
    setActiveIndex((current) => Math.min(current + 1, totalSlides - 1));
  };

  useEffect(() => {
    const handleKeyDown = (event: KeyboardEvent) => {
      if (!totalSlides) return;

      if (event.key === "ArrowLeft") {
        event.preventDefault();
        goToPrevious();
      }

      if (event.key === "ArrowRight") {
        event.preventDefault();
        goToNext();
      }
    };

    window.addEventListener("keydown", handleKeyDown);

    return () => {
      window.removeEventListener("keydown", handleKeyDown);
    };
  }, [totalSlides]);

  if (!activeSlide) {
    return (
      <section className="saved-slides-result">
        <div className="saved-slides-empty">
          <h3>No slides found.</h3>
          <p>
            The saved output exists, but no valid slide list was found in the
            deck payload.
          </p>
        </div>
      </section>
    );
  }

  return (
    <section className="saved-slides-result">
      <div className="saved-slides-header">
        <div>
          <div className="saved-result-kicker">Saved Slides</div>
          <h3 className="saved-result-title">{deckTitle}</h3>
          <p className="saved-result-description">{deckDescription}</p>
        </div>

        <div className="saved-provider-pill">Generated by {provider}</div>
      </div>

      <div className="slides-viewer-shell">
        <div className="slides-viewer-toolbar">
          <div>
            <p className="slides-viewer-count">
              Slide {activeIndex + 1} of {totalSlides}
            </p>
            <p className="slides-viewer-hint">Use ← and → keys to navigate</p>
          </div>

          <div className="slides-viewer-actions">
            <button
              type="button"
              onClick={goToPrevious}
              disabled={activeIndex === 0}
              className="slides-nav-button"
            >
              Previous
            </button>

            <button
              type="button"
              onClick={goToNext}
              disabled={activeIndex === totalSlides - 1}
              className="slides-nav-button slides-nav-button-primary"
            >
              Next
            </button>
          </div>
        </div>

        <div className="slides-progress-track">
          <div
            className="slides-progress-fill"
            style={{ width: `${progressPercent}%` }}
          />
        </div>

        <article className="slides-presentation-card">
          <div className="slides-card-topline">
            <span>Slide {activeSlide.slideNumber}</span>
            <span>{deckTitle}</span>
          </div>

          <div className="slides-card-content">
            <div className="slides-main-panel">
              <h4>{activeSlide.title}</h4>

              {activeSlide.subtitle && (
                <p className="slides-topic-description">
                  {activeSlide.subtitle}
                </p>
              )}

              <ul className="slides-bullet-list">
                {activeSlide.bullets.map((bullet, index) => (
                  <li key={`${activeSlide.slideNumber}-bullet-${index}`}>
                    {bullet}
                  </li>
                ))}
              </ul>
            </div>

            <aside className="slides-side-panel">
              <div className="slides-visual-card">
                <p className="slides-panel-label">Visual Idea</p>
                <p>{activeSlide.visualIdea || "No visual idea provided."}</p>
              </div>

              <button
                type="button"
                onClick={() => setShowSpeakerNotes((current) => !current)}
                className="slides-speaker-button"
              >
                {showSpeakerNotes
                  ? "Hide Speaker Notes"
                  : "Show Speaker Notes"}
              </button>

              {showSpeakerNotes && (
                <div className="slides-speaker-notes-card">
                  <p className="slides-panel-label">Speaker Notes</p>
                  <p>
                    {activeSlide.speakerNotes ||
                      "No speaker notes were generated for this slide."}
                  </p>
                </div>
              )}
            </aside>
          </div>
        </article>

        <div className="slides-thumbnail-strip">
          {slides.map((slide, index) => (
            <button
              key={`${slide.slideNumber}-${slide.title}`}
              type="button"
              onClick={() => {
                setActiveIndex(index);
                setShowSpeakerNotes(false);
              }}
              className={`slides-thumbnail ${
                activeIndex === index ? "slides-thumbnail-active" : ""
              }`}
            >
              <span>{index + 1}</span>
              <p>{slide.title}</p>
            </button>
          ))}
        </div>
      </div>
    </section>
  );
}