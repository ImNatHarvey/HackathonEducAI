import type { ReactNode } from "react";

export type TableColumn = {
  key: string;
  label: string;
};

export type TableRow = Record<string, unknown>;

export const isRecord = (value: unknown): value is Record<string, unknown> => {
  return typeof value === "object" && value !== null;
};

export const normalizeAnswer = (value: string) => {
  return value.trim().toLowerCase().replace(/[^\w\s]/g, "");
};

export const shuffleArray = <T,>(items: T[]) => {
  return [...items].sort(() => Math.random() - 0.5);
};

export const formatProviderName = (provider: string) => {
  return provider
    .split("-")
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(" ");
};

export const getProviderLabel = (result: unknown) => {
  if (!isRecord(result)) return "";

  const provider = typeof result.provider === "string" ? result.provider : "";
  const fallback = result.fallback === true;

  if (!provider && fallback) return "Fallback output";
  if (!provider) return "";

  return fallback
    ? `Generated by ${formatProviderName(provider)} fallback`
    : `Generated by ${formatProviderName(provider)}`;
};

export const ProviderBadge = ({ result }: { result: unknown }) => {
  const label = getProviderLabel(result);

  if (!label) return null;

  return (
    <span className="inline-flex rounded-full border border-aura-gold/40 bg-aura-gold/10 px-3 py-1 text-[10px] font-black uppercase tracking-wider text-aura-gold">
      {label}
    </span>
  );
};

export const FallbackJsonView = ({ result }: { result: unknown }) => {
  return (
    <pre className="aura-scrollbar max-h-[52vh] overflow-auto rounded-2xl border border-aura-border bg-aura-bg px-4 py-4 text-xs leading-6 text-aura-muted">
      {JSON.stringify(result, null, 2)}
    </pre>
  );
};

export const getRowValue = (
  row: TableRow,
  column: TableColumn,
  columnIndex: number,
) => {
  const directValue = row[column.key];

  if (directValue !== undefined && directValue !== null && directValue !== "") {
    return String(directValue);
  }

  const labelValue = row[column.label];

  if (labelValue !== undefined && labelValue !== null && labelValue !== "") {
    return String(labelValue);
  }

  const lowerKey = column.key.toLowerCase();
  const lowerLabel = column.label.toLowerCase();

  const matchingEntry = Object.entries(row).find(([key]) => {
    const normalizedKey = key.toLowerCase().replace(/[^a-z0-9]/g, "_");
    const normalizedLabel = lowerLabel.replace(/[^a-z0-9]/g, "_");

    return (
      normalizedKey === lowerKey ||
      normalizedKey === normalizedLabel ||
      key.toLowerCase() === lowerLabel
    );
  });

  if (matchingEntry?.[1] !== undefined && matchingEntry[1] !== null) {
    return String(matchingEntry[1]);
  }

  const fallbackValue = Object.values(row)[columnIndex];

  if (fallbackValue !== undefined && fallbackValue !== null) {
    return String(fallbackValue);
  }

  return "—";
};

export const ResultHeader = ({
  eyebrow,
  title,
  description,
  result,
}: {
  eyebrow: string;
  title: string;
  description?: ReactNode;
  result: unknown;
}) => {
  return (
    <div className="flex flex-col gap-3 sm:flex-row sm:items-start sm:justify-between">
      <div>
        <p className="text-[10px] font-black uppercase tracking-[0.22em] text-aura-cyan">
          {eyebrow}
        </p>

        <h3 className="mt-1 text-xl font-black text-aura-text">{title}</h3>

        {description && (
          <p className="mt-2 text-sm leading-6 text-aura-muted">
            {description}
          </p>
        )}
      </div>

      <ProviderBadge result={result} />
    </div>
  );
};